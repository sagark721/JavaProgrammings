package Patterns;

public class P17 {

	public static void main(String[] args) {
		int n=4;
		for (int i=0;i<n;i++)
		{
			for (int j=0;j<n;j++) {
				if(i==n-1)
				{
					System.out.print('*');
				}
				
				
			}
			System.out.println();
			
		}
	}

}
